package mx.axa.tarifa.Objetos_Servicios;

public class WS_DatosRiesgos {
	private WS_TipoValor tipoValor;
	private WS_TipoCarga tipoCarga;
	private WS_Uso uso;
	private WS_Servicio servicio;
	public WS_TipoValor getTipoValor() {
		return tipoValor;
	}
	public void setTipoValor(WS_TipoValor tipoValor) {
		this.tipoValor = tipoValor;
	}
	public WS_TipoCarga getTipoCarga() {
		return tipoCarga;
	}
	public void setTipoCarga(WS_TipoCarga tipoCarga) {
		this.tipoCarga = tipoCarga;
	}
	public WS_Uso getUso() {
		return uso;
	}
	public void setUso(WS_Uso uso) {
		this.uso = uso;
	}
	public WS_Servicio getServicio() {
		return servicio;
	}
	public void setServicio(WS_Servicio servicio) {
		this.servicio = servicio;
	}
	
}
